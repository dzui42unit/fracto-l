#include "libft.h"

int		ft_isany(const char *s, int (*f)(int))
{
	int i;

	if (!s || !f)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		if (f(s[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
