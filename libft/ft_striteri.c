#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int index, char *ch))
{
	unsigned int	i;

	i = 0;
	if (s && f)
	{
		while (s[i] != '\0')
		{
			f(i, (s + i));
			i++;
		}
	}
}
